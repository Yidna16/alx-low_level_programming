/**
 * op_add-add a & b
 * @a:parameter
 * @b:parameter
 * Return:sum
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub-substracte a & b
 * @a:parameter
 * @b:parameter
 * Return:substracted
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul-multiply a & b
 * @a:parameter
 * @b:parameter
 * Return:multiplication
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div-divide a & b
 * @a:parameter
 * @b:parameter
 * Return:division
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod-remainder of a & b
 * @a:parameter
 * @b:parameter
 * Return:remander
*/
int op_mod(int a, int b)
{
return (a % b);
}
